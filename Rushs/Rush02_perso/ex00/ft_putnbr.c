#include "fcts.h"

void    ft_putnbr(int n)
{
	if (n < 10)
		ft_putchar(n + 48);
	else
	{
		ft_putnbr((n - (n % 10)) / 10);
		ft_putchar((n % 10) + 48);
	}
}
